    Country(String value, Region region) {
        this.value = value;
        this.region = region;
    }

    @JsonCreator
    public static Country getCountry(String value) {
        for (Country country : values())
            if (country.getValue().equalsIgnoreCase(value))
                return country;
        throw new ProgramFlowException(INVALID_COUNTRY);
    }

    @JsonValue
    public String getValue() {
        return value;
    }

    @Override
    public String toString() {
        return getValue();
    }

    public Region getRegion() {
        return region;
    }
